#include <stdlib.h>
#include <stdbool.h>

#ifndef FSM_H
#define FSM_H

////////////////////////////////////////////////////////////////////////////////
// DEFINES
////////////////////////////////////////////////////////////////////////////////

enum state {
    STATE_elevatorInit = 0,
    STATE_elevatorStandStill,
    STATE_elevatorGoingUp,
    STATE_elevatorGoingDown,
    STATE_elevatorEmergency,
    STATE_elevatorError
};

struct FSMcontroll{
    enum state previousState;
    enum state currentState;
    enum state nextState;
};

////////////////////////////////////////////////////////////////////////////////
// STRUCTS
////////////////////////////////////////////////////////////////////////////////

extern struct FSMcontroll *fsmControll;

////////////////////////////////////////////////////////////////////////////////
// FUNCTIONS
////////////////////////////////////////////////////////////////////////////////

int fsm_init();
int fsm_cleanup(struct FSMcontroll *fsm);
int fsm_setNextState(struct FSMcontroll *fsm, enum state nextState);

#endif // FSM_H